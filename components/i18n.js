  // Get or set language
  window.getAppLanguage = function(){
    const saved = localStorage.getItem(STORAGE_KEY);
    return saved || defaultLang;
  };

  window.setAppLanguage = function(lang){
    if (translations[lang]){
      localStorage.setItem(STORAGE_KEY, lang);
      window.t = function(key){ return translations[lang][key] || key; };
      applyTranslations(document);
      const event = new CustomEvent('languageChanged', { detail: { language: lang } });
      document.dispatchEvent(event);
    }
  };

  // Translation function
  window.t = function(key){
    const lang = window.getAppLanguage();
    return translations[lang] ? (translations[lang][key] || key) : key;
  };

  // Apply translations to DOM
  function applyTranslations(root){
    const nodes = root.querySelectorAll('[data-i18n]');
    nodes.forEach(n => {
      const key = n.getAttribute('data-i18n');
      const text = window.t(key);
      n.textContent = text;
      if (n.placeholder !== undefined && n.hasAttribute('data-i18n-placeholder')){
        n.placeholder = window.t(n.getAttribute('data-i18n-placeholder'));
      }
      if (n.hasAttribute('data-i18n-value')) n.value = text;
    });
  }

  // Auto-translate new elements
  const observer = new MutationObserver(mutations => {
    let hasNewTranslatableNodes = false;
    mutations.forEach(mutation => {
      if (mutation.addedNodes.length > 0){
        mutation.addedNodes.forEach(node => {
          if (node.nodeType === 1 && (node.hasAttribute && node.hasAttribute('data-i18n') || (node.querySelectorAll && node.querySelectorAll('[data-i18n]').length > 0))){
            hasNewTranslatableNodes = true;
          }
        });
      }
    });
    if (hasNewTranslatableNodes) applyTranslations(document);
  });

  function setupAutoTranslate(){
    observer.observe(document.body, {
      childList: true,
      subtree: true
    });
  }

